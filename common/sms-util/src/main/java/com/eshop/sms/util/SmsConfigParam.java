package com.eshop.sms.util;

/**
 * @author meishuquan
 * @date 2017/5/5.
 */
public class SmsConfigParam {

    private String smsUrl;

    private String smsEncoding;

    private String smsSpCode;

    private String smsLoginName;

    private String smsPassword;

    private String smsSerialNumber;

    private String smsF;

    private String smsTemplate;

    public String getSmsUrl() {
        return smsUrl;
    }

    public void setSmsUrl(String smsUrl) {
        this.smsUrl = smsUrl;
    }

    public String getSmsEncoding() {
        return smsEncoding;
    }

    public void setSmsEncoding(String smsEncoding) {
        this.smsEncoding = smsEncoding;
    }

    public String getSmsSpCode() {
        return smsSpCode;
    }

    public void setSmsSpCode(String smsSpCode) {
        this.smsSpCode = smsSpCode;
    }

    public String getSmsLoginName() {
        return smsLoginName;
    }

    public void setSmsLoginName(String smsLoginName) {
        this.smsLoginName = smsLoginName;
    }

    public String getSmsPassword() {
        return smsPassword;
    }

    public void setSmsPassword(String smsPassword) {
        this.smsPassword = smsPassword;
    }

    public String getSmsSerialNumber() {
        return smsSerialNumber;
    }

    public void setSmsSerialNumber(String smsSerialNumber) {
        this.smsSerialNumber = smsSerialNumber;
    }

    public String getSmsF() {
        return smsF;
    }

    public void setSmsF(String smsF) {
        this.smsF = smsF;
    }

    public String getSmsTemplate() {
        return smsTemplate;
    }

    public void setSmsTemplate(String smsTemplate) {
        this.smsTemplate = smsTemplate;
    }
}
